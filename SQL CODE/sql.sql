CREATE TABLE student(
    id int NOT NULL AUTO_INCREMENT,
    fName varchar(255),
    lName varchar(255),
    pName varchar(255),
    email varchar(255),
    address varchar(255),
    hSchool varchar(255),
    gpa varchar(255),
    gYear varchar(255),
    standing varchar(255),
    major varchar(255)
    PRIMARY KEY (ID)
)

